// ============================================
// Customer Context Prompt
// ============================================

export function getCustomerContextPrompt(context: CustomerContext): string {
  const { totalSpent, averageOrderValue, totalOrders } = context;

  return `
**معلومات العميل الحالي:**
- الاسم: ${context.name}
- نوع الحساب: ${context.priceListName}
- إجمالي الطلبات: ${totalOrders} طلبية
- إجمالي المشتريات: ${totalSpent.toLocaleString('ar-IQ')} د.ع
- متوسط قيمة الطلبية: ${averageOrderValue.toLocaleString('ar-IQ')} د.ع
- آخر طلبية: ${context.lastOrderDate || 'لا يوجد'}
- الرصيد المتبقي: ${context.outstandingBalance.toLocaleString('ar-IQ')} د.ع
${context.creditLimit ? `- حد الائتمان: ${context.creditLimit.toLocaleString('ar-IQ')} د.ع` : ''}

**المنتجات المفضلة للعميل:**
${context.topProducts.map((p, i) => `${i + 1}. ${p.name} (${p.orderCount} مرات)`).join('\n')}

**استخدم هذه المعلومات لـ:**
- تقديم توصيات شخصية بناءً على سجل الشراء
- تذكير العميل بإعادة طلب المنتجات المعتادة
- اقتراح منتجات مشابهة لما يشتريه عادة
- التنبيه إذا كان المخزون منخفض للمنتجات المفضلة
- ضبط لهجة التواصل حسب علاقة العميل (عميل جديد vs عميل قديم)

**ملاحظات:**
- عميل ${totalOrders > 20 ? 'مميز ومخلص' : totalOrders > 5 ? 'دائم' : 'جديد'}
- ${context.outstandingBalance > 0 ? '⚠️ يوجد رصيد مستحق' : '✅ لا يوجد رصيد مستحق'}
`;
}

