// ============================================
// Generate Complete System Prompt
// ============================================

export function generateSystemPrompt(
  customerContext?: CustomerContext
): string {
  let prompt = BASE_IRAQI_PROMPT;

  if (customerContext) {
    prompt += '\n\n' + getCustomerContextPrompt(customerContext);
  }

  prompt += `\n\n**الوظائف المتاحة:**
${Object.entries(FUNCTION_DESCRIPTIONS).map(([name, desc]) => `- ${name}: ${desc}`).join('\n')}

**تذكر:**
- أنت تمثل TSH، شركة محترفة وموثوقة
- الدقة في المعلومات أهم من السرعة
- إذا لم تكن متأكداً من شيء، قل ذلك بوضوح
- عامل كل عميل كأنه شريك تجاري مهم
`;

  return prompt;
}
